    private void ProductImageUploadButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProductImageUploadButtonActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.setDialogTitle("Select the Image file....");
        chooser.setMultiSelectionEnabled(false);
        chooser.setPreferredSize(new Dimension(650, 410));
        int result=chooser.showDialog(null, "Upload");

        if(result==JFileChooser.APPROVE_OPTION)
        {
            String imageLink=chooser.getSelectedFile().getPath();
            imageLink = imageLink.replace("\\", "\\\\");
            ProductImageLink=imageLink;
            mSetImage(ProductImageLink);
        }
    }//GEN-LAST:event_ProductImageUploadButtonActionPerformed

