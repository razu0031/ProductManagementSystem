    private void ProductUpdateOkButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProductUpdateOkButtonActionPerformed

        String productName = ProductName.getText();
        int productPrice = Integer.parseInt(ProductPrice.getText());
        int productDiscount = Integer.parseInt(ProductDiscount.getText());
        String description=ProductDescription.getText();

        if(TASK==UPDATE_PRODUCT)
        {
            ProductName.setEnabled(false);
            if(!AdminUserName.isEmpty() && !SubCategoryName.isEmpty() && !ProductImageLink.isEmpty() && !ProductPrice.getText().trim().isEmpty() && productPrice>0 && !ProductDiscount.getText().trim().isEmpty() && !description.isEmpty())
            {
                try
                {
                    InsertUpdateProduct(SubCategoryName, AdminUserName, productName, ProductImageLink, productDiscount, productPrice, description);
                }
                catch (Exception ex) {
                    Logger.getLogger(CategoryUpdateForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else if(TASK==INSERT_NEW_PRODUCT) 
        {
            if(!AdminUserName.isEmpty() && !SubCategoryName.isEmpty() && !ProductImageLink.isEmpty() && !ProductPrice.getText().trim().isEmpty() && productPrice>0 && !ProductDiscount.getText().trim().isEmpty() && !description.isEmpty())
            {
                try
                {
                   
                    InsertUpdateProduct(SubCategoryName, AdminUserName, productName, ProductImageLink, productDiscount, productPrice, description);
                }
                catch (Exception ex) {
                    Logger.getLogger(CategoryUpdateForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        

    }//GEN-LAST:event_ProductUpdateOkButtonActionPerformed

