    public void InsertUpdateProduct(String categoryName, String adminName, String productName, String productImageLink, int discount, int unitPrice, String productDescription) throws SQLException, Exception
    {
        Connection c=getOracleConnection();
        CallableStatement callStmt = null;
        try 
        {
            callStmt = c.prepareCall("{call InsertUpdateProduct(?,?,?,?,?,?,?)}");
            callStmt.setString(1, categoryName);
            callStmt.setString(2, adminName);
            callStmt.setString(3, productName);
            callStmt.setString(4, productImageLink);
            callStmt.setInt(5, discount);
            callStmt.setInt(6, unitPrice);
            callStmt.setString(7, productDescription);
            
            callStmt.execute();
            
            System.out.println("Product Updated");
  
        } finally {
            callStmt.close();
            c.close();
        }
        
        this.dispose();
    }
    
